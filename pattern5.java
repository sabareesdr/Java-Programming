package Nightclass;
import java.util.*;
public class pattern5 {

	public static void main(String[] args) {
		Scanner obj=new Scanner(System.in);
		int a=obj.nextInt();
		for(int i=0;i<a;i++)
		{
			for(int j=0;j<=i;j++)
			{
				System.out.print("*");
			}	
			System.out.println();
			
		}
		
		
			

	}}
